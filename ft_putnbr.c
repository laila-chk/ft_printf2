#include "ft_printf.h"

void	ft_putnbr(int n)
{
	long	a;

	a = n;
	if (a < 0)
	{
		a = -1 * a;
		ft_putchar('-');
	}
	if (a > 9)
	{
		ft_putnbr(a / 10);
		ft_putnbr(a % 10);
	}
	if (a <= 9)
		ft_putchar(a + 48);
}
