#include "ft_printf.h"

void	ft_putstr(char *str)
{
	int	i;

	i = 0;
	if (!str)
	{
		g_counter += write(1, "(null)", 6);
		return ;
	}
	while (*str)
	{
		ft_putchar(*str);
		str++ ;
	}
}
