#include "ft_printf.h"

void	ft_putnbr2(unsigned int a)
{
	if (a < 0)
	{
		a = -1 * a;
		ft_putchar('-');
	}
	if (a > 9)
	{
		ft_putnbr(a / 10);
		ft_putnbr(a % 10);
	}
	if (a <= 9)
		ft_putchar(a + 48);
}
